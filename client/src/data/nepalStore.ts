const catalogDate = "2026-05-30T10:15:00.000Z";

type ProductInput = Omit<
  Product,
  "_id" | "image" | "stock" | "rating" | "reviewCount" | "discount" | "createdAt"
> & {
  imageIndex: number;
  stock?: number;
  rating?: number;
  reviewCount?: number;
};

const productImage = (index: number) => dummyProducts[index % dummyProducts.length].image;

const makeProduct = (id: string, input: ProductInput): Product => {
  const discount = Math.max(
    0,
    Math.round(((input.originalPrice - input.price) / input.originalPrice) * 100),
  );

  return {
    _id: id,
    name: input.name,
    description: input.description,
    price: input.price,
    originalPrice: input.originalPrice,
    image: productImage(input.imageIndex),
    category: input.category,
    unit: input.unit,
    stock: input.stock ?? 60,
    isOrganic: input.isOrganic,
    rating: input.rating ?? 4.6,
    reviewCount: input.reviewCount ?? 28,
    discount,
    createdAt: catalogDate,
  };
};

